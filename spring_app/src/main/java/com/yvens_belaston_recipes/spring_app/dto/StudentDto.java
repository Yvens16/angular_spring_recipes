package com.yvens_belaston_recipes.spring_app.dto;

public class StudentDto {
  String name;
  int age;

  public StudentDto(String name, int age) {
    this.name = name;
    this.age = age;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public int getAge() {
    return age;
  }

  public void setAge(int age) {
    this.age = age;
  }

}
